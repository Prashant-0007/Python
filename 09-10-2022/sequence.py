print("Demonstration of sequence datatype.")

#list data type enclosed in [] square bracket
listData = [10, 20, 30, 40]

#tuple data type enclosed in () circle bracket
tupleData = (10, 20, 30, 40)

#set data type enclosed in {} curly bracket
setData = {10, 20, 30, 40}

#dictinary data-type enclosed in { curly bracket}
dictdata = {"C":400, "C++":550, "Java":900, "Python":890}

print(listData)
print(tupleData)
print(setData)
print(dictdata)

print(type(listData))
print(type(tupleData))
print(type(setData))
print(type(dictdata))


